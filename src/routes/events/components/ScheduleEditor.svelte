							<div>
								<label class="label">
									<span class="label-text">Activity *</span>
								</label>
								{#if !isCustomActivity}
									<select
										class="select select-bordered w-full"
										onchange={(e) => {
											const value = e.target.value
											if (value === 'Other') {
												isCustomActivity = true
												newBlock.activity = ''
											} else if (value) {
												newBlock.activity = value
												// Set duration from template if available
												const template = activityTemplates.find(t => t.name === value)
												if (template && newBlock.time) {
													newBlock.duration = template.duration
													newBlockEndTime = calculateEndTime(newBlock.time, template.duration)
												}
											}
										}}
									>
										<option value="">Select activity</option>
										{#each activityTemplates as template}
											<option value={template.name}>{template.name}</option>
										{/each}
										<option value="Other">Other (custom)</option>
									</select>
								{/if}
								{#if isCustomActivity}
									<div class="flex gap-2">
										<input
											type="text"
											bind:value={newBlock.activity}
											class="input input-bordered w-full"
											placeholder="Enter custom activity name"
											required
										/>
										<button
											type="button"
											class="btn btn-outline btn-sm"
											onclick={() => {
												isCustomActivity = false
												newBlock.activity = ''
											}}
											title="Use template instead"
										>
											×
										</button>
									</div>
								{/if}
							</div>
							
							<!-- Notes -->
							<div class="md:col-span-3">
								<label class="label">
									<span class="label-text">Notes</span>
								</label>
								<textarea
									bind:value={newBlock.notes}
									class="textarea textarea-bordered w-full"
									rows="2"
									placeholder="Additional notes for this schedule block..."
								></textarea>
							</div>
						</div>
						
						<!-- Form Actions -->
						<div class="flex justify-end gap-2 mt-4">
							<button 
								class="btn btn-outline btn-sm"
								onclick={() => showAddForm = false}
							>
								Cancel
							</button>
							<button 
								class="btn btn-primary btn-sm"
								onclick={addBlock}
								disabled={!newBlock.activity.trim() || !newBlock.time || !newBlockEndTime}
							>
								Add Block
							</button>
						</div>
					</div>
				</div>
			{/if}

			<!-- Schedule Blocks List -->
			{#if scheduleBlocks.length > 0}
				<div class="space-y-2">
					{#each scheduleBlocks as block, index}
						<div class="card bg-base-100 border {block.is_auto_generated ? 'border-primary/30' : ''}">
							<div class="card-body p-4">
								<div class="flex items-start justify-between">
									<div class="flex-1">
										{#if editingIndex === index}
											<!-- Edit Mode -->
											<div class="grid grid-cols-1 md:grid-cols-3 gap-4">
												<div>
													<label class="label">
														<span class="label-text">Start Time</span>
													</label>
													<input
														type="time"
														bind:value={block.time}
														class="input input-bordered input-sm w-full"
														onchange={() => {
															if (block.time && editingBlockEndTime) {
																block.duration = calculateDurationMinutes(block.time, editingBlockEndTime)
															}
														}}
													/>
												</div>
												<div>
													<label class="label">
														<span class="label-text">End Time</span>
													</label>
													<input
														type="time"
														bind:value={editingBlockEndTime}
														class="input input-bordered input-sm w-full"
														onchange={() => {
															if (block.time && editingBlockEndTime) {
																block.duration = calculateDurationMinutes(block.time, editingBlockEndTime)
															}
														}}
													/>
													{#if block.time && editingBlockEndTime}
														<div class="text-xs opacity-60 mt-1">
															Duration: {formatDuration(calculateDurationMinutes(block.time, editingBlockEndTime))}
														</div>
													{/if}
												</div>
												<div>
													<label class="label">
														<span class="label-text">Activity</span>
													</label>
													<input
														type="text"
														bind:value={block.activity}
														class="input input-bordered input-sm w-full"
														placeholder="Enter activity name"
													/>
												</div>
												<div class="md:col-span-3">
													<label class="label">
														<span class="label-text">Notes</span>
													</label>
													<textarea
														bind:value={block.notes}
														class="textarea textarea-bordered textarea-sm w-full"
														rows="2"
													></textarea>
												</div>
											</div>
										{:else}
											<!-- View Mode -->
											<div class="flex items-center gap-4 flex-wrap">
												{#if block.is_auto_generated}
													<div class="badge badge-primary badge-sm">Auto</div>
												{/if}
												{#if block.artist_name}
													<div class="badge badge-outline">{block.artist_name}</div>
												{/if}
												{#if block.time}
													<div class="text-sm font-mono bg-base-200 px-2 py-1 rounded">
														{block.time} - {calculateEndTime(block.time, block.duration)}
													</div>
												{/if}
												<div class="badge badge-outline">
													{formatDuration(block.duration)}
												</div>
												<div class="font-medium">{block.activity}</div>
											</div>
											{#if block.notes}
												<p class="text-sm opacity-70 mt-2">{block.notes}</p>
											{/if}
										{/if}
									</div>
									
									{#if !readonly}
										<div class="flex items-center gap-1 ml-4">
											{#if editingIndex === index}
												<button 
													class="btn btn-xs btn-success"
													onclick={() => saveEdit(index)}
												>
													Save
												</button>
												<button 
													class="btn btn-xs btn-outline"
													onclick={cancelEdit}
												>
													Cancel
												</button>
											{:else}
												<button 
													class="btn btn-xs btn-outline"
													onclick={() => moveBlock(index, 'up')}
													disabled={index === 0}
												>
													↑
												</button>
												<button 
													class="btn btn-xs btn-outline"
													onclick={() => moveBlock(index, 'down')}
													disabled={index === scheduleBlocks.length - 1}
												>
													↓
												</button>
												<button 
													class="btn btn-xs btn-outline"
													onclick={() => startEdit(index)}
												>
													Edit
												</button>
												<button 
													class="btn btn-xs btn-error"
													onclick={() => removeBlock(index)}
												>
													×
												</button>
											{/if}
										</div>
									{/if}
								</div>
							</div>
						</div>
					{/each}
				</div>
			{:else}
				<div class="text-center py-8 bg-base-200 rounded-lg">
					<Calendar class="w-16 h-16 mx-auto text-base-content/70" />
					<p class="mt-2 text-lg">No schedule blocks</p>
					<p class="text-sm opacity-60">
						{readonly ? 'No schedule defined for this event' : 'Click "Add Block" to create a schedule'}
					</p>
				</div>
			{/if}
		{/if}
	{/if}
</div>