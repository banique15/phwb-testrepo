							<div class="flex flex-wrap gap-2">
								{#each availableArtists as artist}
									<button
										class="btn btn-xs btn-outline"
										onclick={() => addExtraBlockForArtist(artist.id, artist.name)}
									>
										<Plus class="w-3 h-3 mr-1" />
										{artist.name} (+1hr prep)
									</button>
								{/each}
							</div>
						</div>
					{/if}
				</div>
			</div>
		{:else}
			<!-- List View -->
		
			<!-- Add Block Form -->
			{#if showAddForm && !readonly}
				<div class="card bg-base-200 border">
					<div class="card-body p-4">
						<h5 class="card-title text-base">Add Schedule Block</h5>
						
						<!-- Templates -->
						<div class="mb-4">
							<label class="label">
								<span class="label-text">Quick Templates</span>
							</label>
							<div class="flex flex-wrap gap-2">
								{#each activityTemplates as template}
									<button
										type="button"
										class="btn btn-xs btn-outline"
										onclick={() => useTemplate(template)}
									>
										{template.name} ({template.duration}m)
									</button>
								{/each}
							</div>
						</div>
						
						<div class="grid grid-cols-1 md:grid-cols-3 gap-4">
							<!-- Artist (optional) -->
							{#if availableArtists.length > 0}
								<div>
									<label class="label">
										<span class="label-text">Artist (optional)</span>
									</label>
									<select
										bind:value={newBlock.artist_id}
										class="select select-bordered w-full"
										onchange={(e) => {
											const selected = availableArtists.find(a => a.id === e.target.value)
											if (selected) {
												newBlock.artist_name = selected.name
											}
										}}
									>
										<option value="">None</option>
										{#each availableArtists as artist}
											<option value={artist.id}>{artist.name}</option>
										{/each}
									</select>
								</div>
							{/if}
							
							<!-- Start Time -->
							<div>
								<label class="label">
									<span class="label-text">Start Time *</span>
								</label>
