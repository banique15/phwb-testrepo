								{#if newBlock.time && newBlockEndTime}
									<div class="text-xs opacity-60 mt-1">
										Duration: {formatDuration(calculateDurationMinutes(newBlock.time, newBlockEndTime))}
									</div>
								{/if}
							</div>
							
							<!-- Duration (alternative input, hidden but kept for backwards compatibility) -->
							<div class="hidden">
								<input
									type="number"
									bind:value={newBlock.duration}
									min="1"
									step="5"
